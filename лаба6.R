# –õ–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–∞—è —Ä–∞–±–æ—Ç–∞ 6: –û—Ü–µ–Ω–∏–≤–∞–Ω–∏–µ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ –≥–µ–æ–º–µ—Ç—Ä–∏—á–µ—Å–∫–æ–≥–æ –±—Ä–æ—É–Ω–æ–≤—Å–∫–æ–≥–æ –¥–≤–∏–∂–µ–Ω–∏—è

# 1. –ó–∞–º–æ–¥–µ–ª–∏—Ä–æ–≤–∞—Ç—å –ø—Ä–æ—Ü–µ—Å—Å (10) –¥–ª—è S0 = 100, a = 0.5, ùúé = 0.8, ‚àö‚àÜ = 0.01, k = 0,1,...,10^3. ----
S_0 = S = 100
a = 0.5
sigma = 0.8
delta = 0.0001
n = 1000
e = rnorm(n, 0, sqrt(delta)) 
B = 0
for(k in 1 : n)
{
  B[k + 1] = B[k] + e[k]  
  # –º–æ–¥–µ–ª–∏—Ä–æ–≤–∞–Ω–∏–µ –ø—Ä–æ—Ü–µ—Å—Å–∞ –ø—Ä–∏ –∑–∞–¥–∞–Ω–Ω—ã—Ö –ø–∞—Ä–∞–º–µ—Ç—Ä–∞—Ö
  S[k + 1] = S_0*exp((a - ((sigma^2)/2))*(k + 1)*delta + sigma*B[k + 1]) 
}
plot(seq(0, delta*1000, delta), S, type = 'l', main = "–†–µ–∞–ª–∏–∑–∞—Ü–∏—è –≥–µ–æ–º–µ—Ç—Ä–∏—á–µ—Å–∫–æ–≥–æ –±—Ä–æ—É–Ω–æ–≤—Å–∫–æ–≥–æ –¥–≤–∏–∂–µ–Ω–∏—è")

# 2. –†–∞—Å—Å—á–∏—Ç–∞—Ç—å –æ—Ü–µ–Ω–∫–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ –±—Ä–æ—É–Ω–æ–≤—Å–∫–æ–≥–æ –¥–≤–∏–∂–µ–Ω–∏—è –ø–æ –Ω–∞–±–ª—é–¥–µ–Ω–∏—è–º –ø—Ä–æ—Ü–µ—Å—Å–∞ {ùëÜùëò‚àÜ}ùëò‚â•1 ----
X = 0
for(k in 2 : 1000)
  X[k - 1] = log(S[k]) - log(S[k - 1])

# –û—Ü–µ–Ω–∫–∏ –≤–æ–ª–∞—Ç–∏–ª—å–Ω–æ—Å—Ç–∏ –∏ —Å–Ω–æ—Å–∞: 
Sum = var(X)
sigma_1 = Sum/delta; sigma_1 # ùúé^2 = 0.64
Œº = (1/n)*sum(X)
a_1 = Œº/delta + sigma_1/2; a_1 # a = 0.5
